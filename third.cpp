int main()
{
	int n; cin>>n;
	int town[n][2];
	int m1,m2;
	for (int i = 0; i < n; i++)
	{
		cin>>m1>>m2;
		town[i][0]=m1;
		town[i][1]=m2;
	}
	
    int ysum(0);
	for (int i = 0; i < n; i++)
	{
		ysum += town[i][0];
	}
	int xsum[n];

    for (int i = 0; i < n; i++)
	{ 
		xsum[i] = town[i][0]+town[i][1];  
	}

	int si = sizeof(xsum) / sizeof(xsum[0]);
	sort(xsum, xsum + si,greater<int>());
	

    int k(0),tsum(0),camp(0);

	while (tsum <= ysum)
	{
		tsum += xsum[k];
		k++;
		camp++;
	}
    cout<<camp;

	return 0;
}